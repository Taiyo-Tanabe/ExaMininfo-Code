/**
 * 漢字テキストからひらがな読みを生成する（近似値）
 * yomiデータがないときのフォールバック用
 */
export function getReading(text) {
  if (!text) return ''
  let result = text
  for (const [kanji, hira] of READINGS) {
    result = result.split(kanji).join(hira)
  }
  return result
}

/** 都道府県名のひらがな読みを返す（完全一致） */
const PREF_READINGS = Object.fromEntries(
  READINGS.filter(([k]) => k.endsWith('道') || k.endsWith('都') || k.endsWith('府') || k.endsWith('県') ||
    ['北海道','青森','岩手','宮城','秋田','山形','福島','茨城','栃木','群馬','埼玉','千葉',
     '東京','神奈川','新潟','富山','石川','福井','山梨','長野','静岡','愛知','三重','滋賀',
     '京都','大阪','兵庫','奈良','和歌山','鳥取','島根','岡山','広島','山口','徳島','香川',
     '愛媛','高知','福岡','佐賀','長崎','熊本','大分','宮崎','鹿児島','沖縄'].includes(k)
  )
)

export function getPrefectureReading(pref) {
  if (!pref) return ''
  // 完全一致
  if (PREF_READINGS[pref]) return PREF_READINGS[pref]
  // 県名の部分（「東京都」→「東京」を検索）
  for (const [k, v] of READINGS) {
    if (pref.startsWith(k)) return v
  }
  return ''
}
